void m2e_angle(struct angle *theta){
	// don't use in speed loop
	theta->deg *= pole_pair*gear_ratio;
	theta->rad *= pole_pair*gear_ratio;

	//the following eq. only valid for vds = 0
	theta->deg += 90;
	if(theta->deg >= 360) theta->deg -= 360;
	theta->rad += M_PI/2;
}

